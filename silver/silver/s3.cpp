#include <bits/stdc++.h>
using namespace std;
using ll = long long;

ll gcd(ll x, ll y)
{
    while (y)
    {
        x %= y;
        swap(x, y);
    }
    return x;
}

void solve()
{
    ll a, b, c, d;
    cin >> a >> b >> c >> d;
    if (c < a || d < b || gcd(a, b) != gcd(c, d))
    {
        cout << -1 << "\n";
        return;
    }
    ll moves = 0;
    while (!(c == a && d == b))
    {
        if (c < a || d < b)
        {
            moves = -1;
            break;
        }
        if (c == d)
        {
            moves = -1;
            break;
        }
        if (c > d)
        {
            ll k = (c - a) / d;
            if (k < 1)
                k = 1;
            c -= k * d;
            moves += k;
        }
        else
        {
            ll k = (d - b) / c;
            if (k < 1)
                k = 1;
            d -= k * c;
            moves += k;
        }
    }
    cout << moves << "\n";
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}