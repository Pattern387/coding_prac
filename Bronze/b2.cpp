#include <bits/stdc++.h>
using namespace std;
using ll = long long;

void solve()
{
    int N;
    cin >> N;
    vector<int> arr(N);
    for (int i = 0; i < N; i++)
    {
        cin >> arr[i];
    }
    vector<int> freq(N + 1, 0);
    for (int i = 0; i < N; i++)
    {
        freq[arr[i]]++;
    }
    vector<int> missing(N + 1, 0);
    missing[0] = 0;
    for (int m = 1; m <= N; m++)
    {
        missing[m] = missing[m - 1] + (freq[m - 1] == 0);
    }
    for (int m = 0; m <= N; m++)
    {
        cout << max(freq[m], missing[m]) << "\n";
    }
}

int main()
{

    solve();
    return 0;
}
